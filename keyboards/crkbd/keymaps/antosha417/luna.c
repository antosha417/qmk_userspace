void luna_update_keypress_timer(void) {
  luna.keypress_timer = timer_read32();
}

void luna_jump(void) {
  luna.isJumping  = true;
  luna.showedJump = false;
}

void luna_stop_jumping(void) {
  luna.isJumping  = false;
}

bool luna_sould_jump(void) {
  return luna.isJumping || !luna.showedJump;
}

const char* get_action(void) {
  int current_wpm = get_current_wpm();
  const char* action = sit[luna.current_frame];
  if (luna.isSneaking) {
    action = sneak[luna.current_frame];
  } else if (current_wpm >= BARK_SPEED) {
    action = bark[luna.current_frame];
  } else if (current_wpm >= RUN_SPEED) {
    action = run[luna.current_frame];
  } else if (current_wpm >= WALK_SPEED) {
    action = walk[luna.current_frame];
  }
  return action;
}

void update_current_frame(void) {
  luna.current_frame = (luna.current_frame + 1) % 2;
}

void clear_bottom(void) {
  oled_set_cursor(luna.x, luna.y + 2);
  oled_write("     ", false);
}

void clear_top(void) {
  oled_set_cursor(luna.x, luna.y - 1);
  oled_write("     ", false);
}

void clear_screen(void) {
  if (luna_sould_jump()) {
    clear_bottom();
  } else {
    clear_top();
  }
}

void set_cursor(void) {
  if (luna_sould_jump()) {
    oled_set_cursor(luna.x, luna.y - 1);
    luna.showedJump = true;
  } else {
    oled_set_cursor(luna.x, luna.y);
  }
}

void draw(const char* action) {
  set_cursor();
  oled_write_raw_P(action, ANIMATION_SIZE);
}

void animate_luna(void) {
  update_current_frame();
  const char* action = get_action();
  clear_screen();
  draw(action);
}

static void render_luna(void) {
    if (timer_elapsed32(luna.keypress_timer) > OLED_TIMEOUT/2) {
      draw(lay);
      return;
    }
    if (timer_elapsed32(luna.animation_timer) > ANIMATION_FRAME_DURATION) {
        luna.animation_timer = timer_read32();
        animate_luna();
    }
}
